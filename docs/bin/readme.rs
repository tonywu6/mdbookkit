use std::process::{Command, Stdio};

use anyhow::{Result, bail};
use mdbookkit::book::BookToml;
use serde_json::json;
use tap::Pipe;
use tempfile::TempDir;
use url::Url;

use crate::CARGO_WORKSPACE;

pub fn render() -> Result<()> {
    let temp_dir = TempDir::new()?;

    let site_url = (CARGO_WORKSPACE.join("docs/book.toml"))
        .pipe(std::fs::read_to_string)?
        .parse::<BookToml>()?
        .html_config::<Url>("site-url")?
        .unwrap();

    Command::new("cargo")
        .args(["bin", "mdbook", "build"])
        .arg("-d")
        .arg(temp_dir.path())
        .env("MDBOOK_output", json!({ "markdown": {} }).to_string())
        .env(
            "MDBOOK_preprocessor__permalinks__site_url",
            site_url.to_string(),
        )
        .env(
            "MDBOOK_preprocessor__permalinks__qualify_book_links",
            "true",
        )
        .env("CI", "1")
        .env("CARGO_TERM_QUIET", "true")
        .stdout(Stdio::inherit())
        .stderr(Stdio::inherit())
        .output()?
        .status
        .pipe(|status| {
            if status.success() {
                Ok(())
            } else {
                bail!("build failed")
            }
        })?;

    for (source, target) in [
        ("index.md", "README.md"),
        ("permalinks/index.md", "crates/mdbook-permalinks/README.md"),
        (
            "rustdoc-links/index.md",
            "crates/mdbook-rustdoc-links/README.md",
        ),
    ] {
        let output = std::fs::read_to_string(temp_dir.path().join(source))?;
        let output = format!("<!-- generated by running `just docs readme` -->\n\n{output}\n");
        std::fs::write(CARGO_WORKSPACE.join(target), output)?;
    }

    Ok(())
}
